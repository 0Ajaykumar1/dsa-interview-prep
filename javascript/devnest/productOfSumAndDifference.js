// You are given two integers, a and b.
// Compute the Sum and Difference of a and b. Return the Product of Sum and Difference.

// Input Format
// First Parameter - Integer a

// Second Parameter - Integer b

// Output Format
// Return the integer.

function solve(a, b) {
    // CODE HERE
    return ((a + b) * (a - b));
}

console.log(solve(5, 2));